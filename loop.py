# Using for loop to run through all entries in a list
ethics = ['respect', 'humility', 'hardwork', 'team'] # define a list
for this_is_ethical in ethics: # define a for loop
    print(this_is_ethical.title() + ', is required at work' + '.\n') # print the results
print('Thank You')

# Any lines of code after for loop that is not indented are executed once without repetition




